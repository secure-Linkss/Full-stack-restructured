def get_country_flag(country_name: str) -> str:
    """
    Get flag emoji for a country name.
    
    Args:
        country_name: Name of the country
        
    Returns:
        Flag emoji string or 🌍 for unknown countries
    """
    if not country_name:
        return "🌍"
    
    # Try exact match first
    if country_name in COUNTRY_FLAGS:
        return COUNTRY_FLAGS[country_name]
    
    # Try case-insensitive match
    country_lower = country_name.lower()
    for key, value in COUNTRY_FLAGS.items():
        if key.lower() == country_lower:
            return value
    
    # Return default globe emoji for unknown countries
    return "🌍"

def get_all_countries():
    """Get list of all supported countries"""
    return sorted([k for k in COUNTRY_FLAGS.keys() if k not in ["Unknown", "Other", ""]])

def get_country_count():
    """Get total count of supported countries"""
    return len([k for k in COUNTRY_FLAGS.keys() if k not in ["Unknown", "Other", ""]])
